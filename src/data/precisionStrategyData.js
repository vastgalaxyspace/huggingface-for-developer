function getFitBadge(requiredGb, availableGb) {
  if (!Number.isFinite(requiredGb)) {
    return 'estimation unavailable';
  }

  if (requiredGb <= 8 && availableGb <= 8) return 'fits 8GB';
  if (requiredGb <= 8 && availableGb > 8) return 'fits 8GB';
  if (requiredGb <= 16) return 'fits 16GB';
  return '24GB+ recommended';
}

export function estimateModelMemory({
  paramsB,
  precisionId,
  includeRuntimeOverhead = true,
  contextLength = 4096,
  batchSize = 1,
}) {
  const entry = precisionEntries.find((item) => item.id === precisionId) || precisionEntries.find((item) => item.id === 'fp16');
  const weightsGb = paramsB * entry.bytesPerParamApprox;
  const runtimeAdjustedGb = includeRuntimeOverhead
    ? weightsGb * memoryFormula.defaultRuntimeOverheadMultiplier
    : weightsGb;
  const contextGb = contextLength * Math.max(batchSize, 1) * memoryFormula.contextOverheadGbPerTokenBatch;

  return {
    weightsGb: Number(weightsGb.toFixed(2)),
    runtimeAdjustedGb: Number(runtimeAdjustedGb.toFixed(2)),
    contextGb: Number(contextGb.toFixed(2)),
    totalGb: Number((runtimeAdjustedGb + contextGb).toFixed(2)),
  };
}

function ruleMatches(rule, input) {
  const { conditions } = rule;
  if (conditions.goal && conditions.goal !== input.goal) return false;
  if (conditions.hardwareType && conditions.hardwareType !== input.hardwareType) return false;
  if (conditions.priority && conditions.priority !== input.priority) return false;
  if (Number.isFinite(conditions.minVramGb) && input.vramGb < conditions.minVramGb) return false;
  if (Number.isFinite(conditions.maxVramGb) && input.vramGb > conditions.maxVramGb) return false;
  return true;
}

export function recommendPrecision({
  hardwareType,
  vramGb,
  goal,
  priority,
  modelSizeB,
}) {
  const input = {
    hardwareType,
    vramGb: Number(vramGb),
    goal,
    priority,
  };

  const matchedRule = recommenderRuleMatrix.find((rule) => ruleMatches(rule, input)) || recommenderRuleMatrix[0];

  const precisionByName = new Map(precisionEntries.map((entry) => [entry.displayName, entry]));
  const fallbackEntry = precisionEntries.find((entry) => entry.id === 'int8');
  const recommendedEntry = precisionByName.get(matchedRule.recommended) || fallbackEntry;

  const estimated = estimateModelMemory({
    paramsB: Number(modelSizeB),
    precisionId: recommendedEntry.id,
    includeRuntimeOverhead: true,
    contextLength: 4096,
    batchSize: 1,
  });

  return {
    recommended: matchedRule.recommended,
    alternatives: matchedRule.alternatives,
    why: matchedRule.why,
    qualityWarning: matchedRule.qualityWarning,
    fitBadge: getFitBadge(estimated.totalGb, Number(vramGb)),
  };
}

